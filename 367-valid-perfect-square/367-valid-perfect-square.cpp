class Solution
{
public:
    bool isPerfectSquare(int n)
    {
        bool res = false;
        int low = 1, high = n;
        ll target;
        while (low <= high)
        {
            ll mid = low + (high - low) / 2;
            target = mid * mid;
            if (target == n)
            {
                res = true;
                break;
            }
            else if (target < n)
            {
                low = mid + 1;
            }
            else
            {
                high = mid - 1;
            }
        }
        return res;
    }
};
